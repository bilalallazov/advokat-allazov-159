"""Контент лендинга — структурированные данные на Python."""

from dataclasses import dataclass


@dataclass(frozen=True)
class InfoCard:
    """Кликабельная карточка с подробным разъяснением."""

    title: str
    icon: str
    summary: str
    paragraphs: tuple[str, ...] = ()
    points: tuple[str, ...] = ()
    points_title: str = 'Что важно знать'
    note: str = ''
    delay: int = 0
    highlight: str = ''
    caption: str = ''
    lead: str = ''
    conditions: tuple[str, ...] = ()
    items: tuple[str, ...] = ()


@dataclass(frozen=True)
class ProcessStep:
    number: int
    title: str
    description: str
    paragraphs: tuple[str, ...] = ()
    points: tuple[str, ...] = ()


@dataclass(frozen=True)
class InfographicStep:
    icon: str
    title: str
    description: str
    paragraphs: tuple[str, ...] = ()
    points: tuple[str, ...] = ()


@dataclass(frozen=True)
class Advantage:
    kind: str  # counter | feature
    title: str
    description: str
    value: int | None = None
    icon: str = ''
    delay: int = 0
    wide: bool = False
    paragraphs: tuple[str, ...] = ()
    points: tuple[str, ...] = ()


